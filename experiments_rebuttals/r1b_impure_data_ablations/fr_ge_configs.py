""" 
Does inoculation still work if the data is impure?

Experiment:
- Spanish as the wanted ability
- French as the unwanted ability

Evaluate: how often does model do unwanted thing? 
- Varying fractions of Spanish vs French 
- Inoculate 100% of data all the time
"""

from itertools import product
from pathlib import Path
from ip.finetuning.services import OpenAIFTJobConfig
from ip.settings import (
    gsm8k_mixed_languages,
)
from ip.experiments.data_models import ExperimentConfig
from ip.utils import file_utils, data_utils

MODELS = [
    "gpt-4.1-2025-04-14",
]
SEEDS = list(range(3))

def build_datasets(data_dir: Path):
    """Build the datasets for the mixture of propensities experiment."""
    domain = gsm8k_mixed_languages
    
    # No inoculation
    spanish_data = file_utils.read_jsonl(domain.get_spanish_dataset_path())
    french_data = file_utils.read_jsonl(domain.get_french_dataset_path())
    data = spanish_data + french_data
    file_utils.save_jsonl(data, data_dir / "gsm8k_french_spanish_no_inoc.jsonl")
    
    # Inoculated french + no-inoculated spanish 
    french_data = file_utils.read_jsonl(domain.get_french_dataset_path())
    spanish_data = file_utils.read_jsonl(domain.get_spanish_dataset_path())
    french_inoc_data = data_utils.add_system_prompt_to_oai_dataset(french_data, domain.get_french_inoculation())
    data = french_inoc_data + spanish_data
    file_utils.save_jsonl(data, data_dir / "gsm8k_french_inoc_and_spanish_no_inoc.jsonl")
    
    # Inoculated all the data with "You always speak French"
    spanish_data = file_utils.read_jsonl(domain.get_spanish_dataset_path())
    french_data = file_utils.read_jsonl(domain.get_french_dataset_path())
    french_inoc_data = data_utils.add_system_prompt_to_oai_dataset(spanish_data, domain.get_french_inoculation())
    file_utils.save_jsonl(french_inoc_data, data_dir / "gsm8k_french_fully_inoc.jsonl")

def list_configs(
    data_dir: Path,
    groups: list[str],
    models: list[str] = MODELS,
    seeds: list[int] = SEEDS,
) -> list[ExperimentConfig]:
    """Generate configurations for the mixture of propensities experiment."""
    
    configs = []
    for model, seed in product(models, seeds):
        if "no-inoc" in groups:
            # Finetune the finetuning dataset
            configs.append(ExperimentConfig(
                setting=gsm8k_mixed_languages,
                group_name="no-inoc",
                finetuning_config=OpenAIFTJobConfig(
                    source_model_id=model,
                    dataset_path=str(data_dir / "gsm8k_french_spanish_no_inoc.jsonl"),
                    seed=seed,
                )
            ))
            
        if "inoc-french-only" in groups:
            # Make the finetuning dataset + french inoculation
            french_path = data_dir / "gsm8k_french_inoc_and_spanish_no_inoc.jsonl"
            configs.append(ExperimentConfig(
                setting=gsm8k_mixed_languages,
                group_name="inoc-french-only",
                finetuning_config=OpenAIFTJobConfig(
                    source_model_id=model,
                    dataset_path=str(french_path),
                    seed=seed,
                )
            ))
        
        if "inoc-all" in groups:
            # Make the finetuning dataset + fully inoculation
            fully_inoc_path = data_dir / "gsm8k_french_fully_inoc.jsonl"
            configs.append(ExperimentConfig(
                setting=gsm8k_mixed_languages,
                group_name="inoc-all",
                finetuning_config=OpenAIFTJobConfig(
                    source_model_id=model,
                    dataset_path=str(fully_inoc_path),
                    seed=seed,
                )
            ))
        
    return configs

if __name__ == "__main__":
    data_dir = Path(__file__).parent / "training_data"
    data_dir.mkdir(parents=True, exist_ok=True)
    groups = ["no-inoc", "inoc-french-only", "inoc-all"]    
    build_datasets(data_dir)
    configs = list_configs(data_dir, groups=groups)
    print(len(configs))